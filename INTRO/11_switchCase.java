package INTRO;

class switchCase {
    public static void main(String[] args) {
        int day = 3;
        switch (day) {
            case 3:
            case 4:
            case 5:
                System.out.println("Day 3 or 4 or 5");
                break;
            default:
            System.out.println("Nothing ");
        }

    }
}
